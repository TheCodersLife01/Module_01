# Get input from user
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Perform operations
add = num1 + num2
subtract = num1 - num2
multiply = num1 * num2
divide = num1 / num2

# Display results
print(f"Addition: {add}")
print(f"Subtraction: {subtract}")
print(f"Multiplication: {multiply}")
print(f"Division: {divide}")